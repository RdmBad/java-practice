package day20_forloop;

public class ForLoop1 {
	public static void main(String[] args) {
		//Using for loop print "Love java!" 10 times
		
		for (int i = 1; i <= 10; i++) {
			System.out.println("Love java!");
		}
	
		for (int q = 5; q <= 15; q++) {
			System.out.print(q + ", ");
		}
	
	for (int w = 20; w >= 10; w--) {
		System.out.print(w + ", ");
	}

	
	
	}
}
