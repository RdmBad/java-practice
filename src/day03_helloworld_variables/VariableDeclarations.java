package day03_helloworld_variables;

public class VariableDeclarations {
	public static void main (String[] args) {
		
		//Declare single variable
		int apples;
		
		//Declare multiple variables in single statements
		int bananas, oranges, mango;
		
		//Declare variable and assign value in single statement
		int kiwi = 14;	
		System.out.println(kiwi);
		
		//Declare and assign value to multiple variables
		//in single statements
		int watermelon = 11, grapes = 100, cherries = 12, apricot;
	
		//  3 IMPORTANT THINGS ABOUT VARIABLES:
		// 1) Name --> CASE SENSITIVE
		// 2) DATA TYPE/TYPE --> int. String etc
		// 3) VALUE in it
	}
}
